//:(¿Por qué no?):
//:¿Por qué no se recomienda usar emojis para los nombres? Uno de los motivos es que puede ser difícil diferenciar rápidamente un emoji. Otra razón es que puede no haber un emoji para cada caso. ¿Qué sucedería si alguien llevara una tarántula al espectáculo de mascotas? No habría ningún emoji para usar en ese caso. Las palabras son mucho más flexibles y confiables.
