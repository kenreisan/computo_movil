//: ## Jerga de programadores
//: Ser desarrollador de apps implica mucho más que determinar qué escribir en Xcode. Los programadores trabajan en conjunto, comentan formas de resolver problemas y comparten sus respectivas explicaciones. Por eso, es importante aprender la terminología que usan los programadores.
//:
//: En este curso, no solo aprenderás a programar y a crear apps, sino también los términos que debes usar para hablar sobre programación con otros desarrolladores.
//:
//: Por ejemplo, una “línea de código” se conoce formalmente como _instrucción_.
//:
//: Ya has visto los términos _constante_ e _identificador_ en este playground. Observa en detalle la siguiente instrucción para entender otros términos.
let numberOfDogs = 101
//: Cuando defines una constante en Swift, eso se denomina _declarar_ una constante. Por lo tanto, la instrucción anterior se conoce como _declaración_. “Declaro que el nombre `numberOfDogs` (cantidad de perros) tiene el valor `101`”.
//:
//: La palabra `let` es un ejemplo de _palabra clave_. Las palabras clave tienen un significado especial en Swift y no se pueden usar como nombres. La palabra clave `let` se usa para declarar una constante. Más adelante, conocerás más palabras clave de Swift.
//:
//: La parte de la instrucción `= 101` es donde _asignas_ un valor a la constante. Esto se denomina _asignación_, y el signo igual `=` se denomina _operador de asignación.
//:
//: - note:(Nota): \
//:No te preocupes tanto por memorizar cada término nuevo de inmediato. Puede ser algo abrumador. Verás los términos usados en contexto durante el resto del curso y, si necesitas refrescar la memoria, puedes buscarlos en el Glosario de la Guía para estudiantes.
