//: ## Problema de mascotas
//: Imagina que tu amigo organizará un espectáculo de mascotas y necesita realizar un seguimiento de la cantidad de animales de cada tipo que participarán.
//: Además, necesita saber la cantidad total de animales y cuántos mamíferos habrá en el espectáculo.
//:
//: Ahora, imagina que solo cuentas con una computadora portátil con Xcode. Tal vez puedas usar un playground para ayudar a tu amigo.
//:
//: Usa lo que has aprendido hasta ahora para realizar un seguimiento de la cantidad de animales de cada tipo mediante un comentario y un número, por ejemplo:
// Number Of Dogs (Cantidad de perros)
5

// Number Of Cats (Cantidad de gatos)
4

// Number Of Turtles (Cantidad de tortugas)
3
//: Luego, puedes copiar esos números en nuevas líneas de código para que el playground realice los cálculos correspondientes, por ejemplo:
// Total Number Of Animals (Cantidad total de animales)
5 + 4 + 3

// Total Number Of Mammals (Cantidad total de mamíferos)
5 + 4
//: - experiment:(Experimento): \
//:A medida que transcurre la semana, obtienes más información sobre las mascotas que participarán en el espectáculo.\
//:Cambia el código anterior para actualizar los totales.
//:
//: Por ejemplo, si otra persona llevará un perro, cambia `Number Of Dogs (Cantidad de perros)` de `5` a `6` y cambia la cantidad de `5` a `6` en todos los demás casos en que aparezca (`Total Number Of Animals` [Cantidad total de animales] y `Total Number Of Mammals` [Cantidad total de mamíferos]).
//:
//: Si alguien llevará otra clase de animal, usa el mismo patrón que usaste para el recuento de los demás animales. Agrega un nuevo comentario (`// Number Of Hamsters` [Cantidad de hámsteres], por ejemplo) en una línea con un número en la siguiente línea. Recuerda actualizar los totales.
//:
//: - Otra persona llevará un perro.
//: - Una de las tortugas se enfermó y no podrá participar.
//: - Otra persona llevará un gato.
//: - Alguien llevará un hámster (un mamífero).
