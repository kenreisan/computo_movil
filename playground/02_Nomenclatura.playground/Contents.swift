//: #### Problema de mascotas
//: Imagina que tu amigo está organizando un espectáculo de mascotas y necesita realizar un seguimiento de la cantidad de cada tipo de animal que llevarán las personas al espectáculo.
//: También necesita realizar un seguimiento de la cantidad total de animales y de la cantidad de mamíferos.
//:
//: Supongamos que solo tienes una computadora portátil con Xcode Playgrounds. Tal vez podamos ayudar a tu amigo.
//:
//: Con los conocimientos que adquiriste hasta ahora, podrías realizar un seguimiento de la cantidad de cada tipo de animal con un comentario y un número, de la siguiente manera:

// Number Of Dogs (Cantidad de perros)
5

// Number Of Cats (Cantidad de gatos)
4

// Number Of Turtles (Cantidad de tortugas)
3

//: Luego, podrías copiar esos números y dejar que el playground haga los cálculos, de la siguiente manera:

// Total Number Of Animals (Cantidad total de animales)
5 + 4 + 3

// Total Number Of Mammals (Cantidad total de mamíferos)
5 + 4

//: - experiment:(Experimento): \
//:A medida que transcurre la semana, obtienes más información sobre las mascotas de las personas. Actualiza el código anterior con los cambios.
