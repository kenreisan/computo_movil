//: Por ejemplo, si otra persona llevará un perro, registra el cambio actualizando `Number Of Dogs (Cantidad de perros)` de `5` a `6` y cambia el `5` por un `6`` en todos los demás casos en que aparezca (Total Number Of Animals [Cantidad total de animales]` y `Total Number Of Mammals [Cantidad total de mamíferos]).`
